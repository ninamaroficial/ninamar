export function getDepartments() {
  return colombiaDepartments.map(dept => ({
    id: dept.id,
    name: dept.name
  }))
}

export function getCitiesByDepartment(departmentName: string) {
  const department = colombiaDepartments.find(d => d.name === departmentName)
  return department ? department.cities.map((city, index) => ({
    id: index + 1,
    name: city
  })) : []
}